    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed

        /// creo una instacia para el objecto
        CuerpoAgua Ca= new CuerpoAgua();
        /// creo la connexion
        Connection Con = Conexion.Conectar();
        String sql = "select * from CuerpoAgua where ID=? ";
        ResultSet rs;/// ESTE ES EL QUE TIENE EL RESULTADO DE LA CONSULTA
        try {
            PreparedStatement ps = Con.prepareStatement(sql);
            ps.setInt(1, Integer.parseInt(txtID2.getText())); // pido el Dato a buscar en la base  de datos
            rs=ps.executeQuery();
            if (rs.next()) {
                /// les pido los Datos guardados del Dato que mandaron a buscar en la base de datos
                Ca.setNombreCuerpoAgua(rs.getString("Nombre"));
                Ca.setID(rs.getInt("ID"));
                Ca.setMuncipio(rs.getString("Municipio"));
                Ca.setTipoCuerpoAgua(rs.getString("TipoCuerpoAgua"));
                Ca.setTipoAgua(rs.getString("TipoAgua"));
                Ca.setClasIRCA(rs.getDouble("IRCA"));

                /// le doy los Datos a los text field de la pestaña Editar/Eliminar
                txtNombre2.setText(Ca.getNombreCuerpoAgua());
                txtID3.setText(String.valueOf(Ca.getID()));
                txtMunicipio2.setText(Ca.getMuncipio());
                txtTipoCuerpoAgua2.setText(Ca.getTipoCuerpoAgua());
                txtTipoAgua2.setText(Ca.getTipoAgua());
                txtIRCA2.setText(String.valueOf(Ca.getClasIRCA()));
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
        /// cierro conexion
        try {
            Con.close()
            ;
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_BuscarActionPerformed

