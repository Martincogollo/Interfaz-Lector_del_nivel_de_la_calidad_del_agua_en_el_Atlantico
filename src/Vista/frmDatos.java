        ///su número identificador.
        Double menor=999999999.0;
        int id=0;
        String Nombre="";
        for (int i = 0; i <VectorDatos.length ; i++) {
            if (VectorDatos[i].getClasIRCA()< menor) {
                menor=VectorDatos[i].getClasIRCA();
                id=VectorDatos[i].getID();
                Nombre=VectorDatos[i].getNombreCuerpoAgua();
            }
        }
        txtSalida.setText(txtSalida.getText()+Nombre+" "+id);
